export function getBaseUrl(environment: 'development' | 'staging' | 'production' = 'development'): string {
  const urls = {
    development: 'http://localhost:3001',
    staging: 'https://api-staging.docfusion.com',
    production: 'https://api.docfusion.com',
  };
  return urls[environment];
}

export function createQueryString(params: Record<string, any>): string {
  const filtered = Object.entries(params).filter(([, v]) => v !== undefined && v !== null);
  return new URLSearchParams(filtered.map(([k, v]) => [k, String(v)])).toString();
}
